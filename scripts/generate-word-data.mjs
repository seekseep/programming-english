const validateDataset = (name, entries) => {
  if (entries.length < 50) {
    throw new Error(`${name} must contain at least 50 words`);
  }

  const seen = new Set();

  for (const entry of entries) {
    if (entry.word.length < 3) {
      throw new Error(`${name} contains a word shorter than 3 characters: ${entry.word}`);
    }

    const key = entry.word.toLowerCase();
    if (seen.has(key)) {
      throw new Error(`${name} contains a duplicate word: ${entry.word}`);
    }

    seen.add(key);
  }
};

const outputDir = join(process.cwd(), 'data', 'words');
mkdirSync(outputDir, { recursive: true });

for (const [name, entries] of Object.entries(datasets)) {
  validateDataset(name, entries);

  const sorted = [...entries].sort((left, right) =>
    left.word.localeCompare(right.word, 'en')
  );

  writeFileSync(
    join(outputDir, `${name}.json`),
    `${JSON.stringify(sorted, null, 2)}\n`
  );
}

const merged = new Map();

for (const entries of Object.values(datasets)) {
  for (const entry of entries) {
    const key = entry.word.toLowerCase();
    const current = merged.get(key);

    if (!current || entry.description.length > current.description.length) {
      merged.set(key, entry);
    }
  }
}

const allEntries = [...merged.values()].sort((left, right) =>
  left.word.localeCompare(right.word, 'en')
);

writeFileSync(join(outputDir, 'all.json'), `${JSON.stringify(allEntries, null, 2)}\n`);

